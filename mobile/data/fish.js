export function getFishById(id) {
  return fishList.find((f) => f.id === id) || null;
}

export function searchFish(query, region) {
  const q = (query || "").trim().toLocaleLowerCase("tr-TR");
  return fishList.filter((f) => {
    const regionOk = !region || region === "Tümü" || f.regions.includes(region);
    if (!regionOk) return false;
    if (!q) return true;
    const hay = [f.name, f.scientific, f.type, f.summary, ...f.regions]
      .join(" ")
      .toLocaleLowerCase("tr-TR");
    return hay.includes(q);
  });
}
