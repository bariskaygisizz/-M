export function getFishById(id) {
  return fishList.find((f) => f.id === id) || null;
}

export function searchFish(query, region) {
  const q = (query || "").trim().toLocaleLowerCase("tr-TR");
  return fishList.filter((f) => {
    const regionOk = !region || region === "Tümü" || f.regions.includes(region);
    if (!regionOk) return false;
    if (!q) return true;
    const hay = [
      f.name,
      f.scientific,
      f.type,
      f.summary,
      ...(f.regions || []),
      ...(f.cities || []),
      ...(f.seas || []),
      ...(f.pairsWith || []),
      ...(f.avoidWith || []),
    ]
      .join(" ")
      .toLocaleLowerCase("tr-TR");
    return hay.includes(q);
  });
}

export function publicFish(f) {
  if (!f) return null;
  const { visual, ...rest } = f;
  return rest;
}

export function fishByOrgan(organId) {
  return fishList
    .filter((f) => f.organs && f.organs[organId])
    .map((f) => ({
      id: f.id,
      name: f.name,
      note: f.organs[organId],
      calories: f.calories,
      omega3: f.omega3,
    }));
}

export function fishByCity(city) {
  const c = String(city || "").toLocaleLowerCase("tr-TR");
  return fishList.filter((f) =>
    (f.cities || []).some((x) => x.toLocaleLowerCase("tr-TR").includes(c))
  );
}
